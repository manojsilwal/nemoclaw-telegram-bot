#!/usr/bin/env node
// Trigger an immediate notification for a specific issue ID
const KEY = 'paperclip_7ab54b7e_5072_48c4_9629_46ab9a8a909f';
const COMPANY_ID = '48f61379-2369-4c6c-adcf-8396f3201c0d';
const BOT_TOKEN = '8614422963:AAF6WYHm1SgaKjb6A3rB2mzKs6hta19VHlo';
const CHAT_ID = '7336081149';
const TELEGRAPH_TOKEN = '921aab26a397f798ca2460cd8e8747f16cca401098d1331297a3a11ecb01';
const ISSUE_ID = process.argv[2];

if (!ISSUE_ID) { console.error('Usage: node trigger-notify.js <issueId>'); process.exit(1); }

async function run() {
  const r = await fetch('http://paperclip:3100/api/issues/' + ISSUE_ID, {
    headers: { 'Authorization': 'Bearer ' + KEY }
  });
  const issue = await r.json();
  console.log('Issue:', issue.title, '| Status:', issue.status);

  const content = [
    { tag: 'p', children: [{ tag: 'em', children: ['Completed | ' + new Date().toLocaleString()] }] },
    { tag: 'h3', children: ['Summary'] },
    { tag: 'p', children: [issue.description || 'No description'] },
    { tag: 'p', children: [{ tag: 'em', children: ['Generated by DreamRise Agent'] }] },
  ];

  const tgph = await fetch('https://api.telegra.ph/createPage', {
    method: 'POST', headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ access_token: TELEGRAPH_TOKEN, title: issue.title.slice(0, 256), author_name: 'DreamRise Agent', content, return_content: false })
  }).then(r => r.json());

  const telegraphUrl = tgph.ok ? tgph.result.url : null;
  console.log('Telegraph:', telegraphUrl || ('FAILED: ' + tgph.error));

  const reportUrl = 'http://34.57.42.63:4000/report/' + ISSUE_ID;
  const viewUrl = telegraphUrl || reportUrl;
  const title = issue.title.replace(/[_*[\]()~`>#+=|{}.!-]/g, '\\$&');
  const text = '\u2705 *Task Completed\\!*\n\n\uD83D\uDCCB *' + title + '*\n\n' + (telegraphUrl ? '\uD83D\uDCD6 [Read Report](' + telegraphUrl + ')' : '\uD83D\uDD17 [View Report](' + reportUrl + ')');

  const tg = await fetch('https://api.telegram.org/bot' + BOT_TOKEN + '/sendMessage', {
    method: 'POST', headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      chat_id: CHAT_ID, text, parse_mode: 'MarkdownV2',
      reply_markup: { inline_keyboard: [[{ text: 'Read Report', url: viewUrl }, { text: 'Open Paperclip', url: 'http://34.57.42.63:3100' }]] }
    })
  }).then(r => r.json());

  console.log('Telegram:', tg.ok ? 'Sent!' : ('FAILED: ' + tg.description));
}

run().catch(e => console.error('Error:', e.message));
